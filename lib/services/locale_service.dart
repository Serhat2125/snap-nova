class LocaleInherited extends InheritedNotifier<LocaleService> {
  final LocaleService service;

  const LocaleInherited({
    super.key,
    required this.service,
    required super.child,
  }) : super(notifier: service);

  static LocaleService of(BuildContext context) {
    return context
        .dependOnInheritedWidgetOfExactType<LocaleInherited>()!
        .service;
  }
}
